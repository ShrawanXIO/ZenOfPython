is_sunny = True
have_umbrella = False
 
# Is it not sunny today?
print("Is it not sunny today?", not is_sunny)

# Do you not have an umbrella?
print("Do you not have an umbrella?", not have_umbrella)

# Should you go for a walk if it's sunny and you don't need an umbrella?
print("Should you go for a walk if it's sunny and you don't need an umbrella?", is_sunny and not have_umbrella)

# Should you go for a walk if it's sunny or if you have an umbrella?
print("Should you go for a walk if it's sunny or if you have an umbrella?", is_sunny or have_umbrella)